package com.example.kotlinders

class Islemler
{
    fun cikarma(a : Int, b : Int) : Int
    {
        return a - b
    }

    fun cikarma(a : Int, b : Int, c : Int) : Int
    {
        return a - b - c
    }

    fun cikarma(a : Int, b : Int, c : Int, d : Int) : Int
    {
        return a - b - c * d
    }

}
